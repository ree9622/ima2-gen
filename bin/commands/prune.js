import { parseArgs } from "../lib/args.js";
import { resolveServer, request } from "../lib/client.js";
import { out, color, die, json, exitCodeForError } from "../lib/output.js";

const SPEC = {
  flags: {
    "trash-ttl":  { type: "string" },
    "failed-ttl": { type: "string" },
    "gen-ttl":    { type: "string" },
    "gen-max-mb": { type: "string" },
    "no-keep-favorites": { type: "boolean" },
    "dry-run":    { short: "n", type: "boolean" },
    "yes":        { short: "y", type: "boolean" },
    "stats":      { type: "boolean" },
    "json":       { type: "boolean" },
    "server":     { type: "string" },
    "help":       { short: "h", type: "boolean" },
  },
};

const HELP = `ima2 prune [options]
  Delete old generated files, soft-deleted trash, and failed-attempt sidecars.

  Defaults are conservative: only trash >7 days and failed >14 days are removed.
  Pass --gen-ttl or --gen-max-mb to also prune successful generations.

Options:
  --stats               Show storage usage and exit (no deletion)
  --trash-ttl <days>    Delete trash older than N days     (default: 7)
  --failed-ttl <days>   Delete failed sidecars older than N days  (default: 14)
  --gen-ttl <days>      Delete generated images older than N days (default: off)
  --gen-max-mb <mb>     Cap total generated bytes; oldest non-favorite first
  --no-keep-favorites   Allow pruning favorites (default: favorites preserved)
  -n, --dry-run         Show what would be deleted without touching disk
  -y, --yes             Skip confirmation prompt for non-dry runs
  --json                Output JSON instead of human text
  --server <url>        Override server URL

Examples:
  ima2 prune --stats
  ima2 prune --dry-run --gen-ttl 30 --gen-max-mb 500
  ima2 prune --gen-ttl 30 --yes
`;

function fmtBytes(n) {
  if (n == null || !Number.isFinite(n)) return "?";
  if (n < 1024) return `${n} B`;
  if (n < 1024 * 1024) return `${(n / 1024).toFixed(1)} KB`;
  if (n < 1024 * 1024 * 1024) return `${(n / 1024 / 1024).toFixed(1)} MB`;
  return `${(n / 1024 / 1024 / 1024).toFixed(2)} GB`;
}

function fmtAge(ms) {
  if (!ms) return "-";
  const days = (Date.now() - ms) / 86_400_000;
  if (days < 1) return `${Math.round(days * 24)}h ago`;
  return `${Math.round(days)}d ago`;
}

function printStats(stats) {
  out(color.bold("Storage usage:"));
  out(`  generated/  ${fmtBytes(stats.generated.bytes).padStart(10)}  ${stats.generated.images} images (${stats.generated.favorites} ★)  oldest ${fmtAge(stats.generated.oldestMs)}`);
  out(`  .trash/     ${fmtBytes(stats.trash.bytes).padStart(10)}  ${stats.trash.files} files            oldest ${fmtAge(stats.trash.oldestMs)}`);
  out(`  .failed/    ${fmtBytes(stats.failed.bytes).padStart(10)}  ${stats.failed.files} sidecars         oldest ${fmtAge(stats.failed.oldestMs)}`);
  out(color.dim(`  total       ${fmtBytes(stats.totalBytes).padStart(10)}`));
}

function printResult(r) {
  const tag = r.dryRun ? color.yellow("[dry-run]") : color.green("[applied]");
  out(`${tag} prune complete`);
  out(`  trash:     ${r.trash.deleted}/${r.trash.scanned} deleted, ${fmtBytes(r.trash.freedBytes)} freed`);
  out(`  failed:    ${r.failed.deleted}/${r.failed.scanned} deleted, ${fmtBytes(r.failed.freedBytes)} freed`);
  out(`  generated: ${r.generated.deleted}/${r.generated.scanned} deleted, ${fmtBytes(r.generated.freedBytes)} freed (skipped ${r.generated.skippedFavorites} favorites)`);
  if (r.generated.nodesTouched) {
    out(color.dim(`  ↳ marked ${r.generated.nodesTouched} node(s) in ${r.generated.sessionsTouched} session(s) as asset-missing`));
  }
}

async function confirm(question) {
  process.stdout.write(question + " [y/N] ");
  return new Promise((resolve) => {
    process.stdin.resume();
    process.stdin.setEncoding("utf-8");
    process.stdin.once("data", (data) => {
      process.stdin.pause();
      resolve(data.trim().toLowerCase() === "y" || data.trim().toLowerCase() === "yes");
    });
  });
}

export default async function pruneCmd(argv) {
  const args = parseArgs(argv, SPEC);
  if (args.help) { out(HELP); return; }

  try {
    const { base } = await resolveServer({ serverFlag: args.server });

    if (args.stats) {
      const stats = await request(base, "/api/storage/stats");
      if (args.json) { json(stats); return; }
      printStats(stats);
      return;
    }

    const num = (v) => (v == null ? null : Number(v));
    const body = {
      trashTtlDays: num(args["trash-ttl"]) ?? 7,
      failedTtlDays: num(args["failed-ttl"]) ?? 14,
      genTtlDays: num(args["gen-ttl"]),
      genMaxMb: num(args["gen-max-mb"]),
      keepFavorites: !args["no-keep-favorites"],
      dryRun: args["dry-run"] === true,
    };

    if (!body.dryRun && !args.yes) {
      const dry = await request(base, "/api/storage/prune", {
        method: "POST",
        body: { ...body, dryRun: true },
      });
      printResult(dry);
      const total = dry.trash.deleted + dry.failed.deleted + dry.generated.deleted;
      if (total === 0) {
        out(color.dim("Nothing to prune."));
        return;
      }
      const ok = await confirm(`Proceed with deletion of ${total} item(s)?`);
      if (!ok) {
        out("Aborted.");
        process.exit(0);
      }
    }

    const result = await request(base, "/api/storage/prune", { method: "POST", body });
    if (args.json) { json(result); return; }
    printResult(result);
  } catch (e) {
    if (args.json) { json({ ok: false, error: e.message }); process.exit(exitCodeForError(e)); }
    die(exitCodeForError(e), e.message);
  }
}
